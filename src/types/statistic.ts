/**
 * Статистическое значение с цветом игрока
 * Используется для отображения метрик (минимум, максимум, среднее) с визуальным выделением
 */
export type GameScoreStatistic = {
  /** Цвет игрока для визуального выделения */
  color: string
  /** Значение статистики (очки, количество и т.д.) */
  score: number
}

/**
 * Ключи для статистики игры
 * - 'min': минимальный результат
 * - 'max': максимальный результат
 * - 'avg': средний результат
 * - 'count': количество партий
 */
export type ScoreStatsKeys = 'min' | 'max' | 'avg' | 'count'

/**
 * Статистика игры по различным метрикам
 * Каждая метрика содержит значение и цвет игрока, достигшего этого значения
 * Все поля опциональны, так как не все метрики могут быть вычислены
 */
export type ScoreStats = Partial<Record<ScoreStatsKeys, GameScoreStatistic>>
